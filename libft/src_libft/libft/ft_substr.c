#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	if (start >= (unsigned int) ft_strlen(s))
		len = 0;
	else if (start + (unsigned int) len > (unsigned int) ft_strlen(s))
		len = ft_strlen(s) - start;
	str = (char *) malloc (sizeof(char) * len + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
